const Shop = () => {
  gsap.registerPlugin(ScrollTrigger);
  const ref = useRef(null);

  const Horizontalref = useRef(null);

  useLayoutEffect(() => {
    let element = ref.current;

    let scrollingElement = Horizontalref.current;

    let pinWrapWidth = scrollingElement.offsetWidth;
    let t1 = gsap.timeline();

    setTimeout(() => {
      t1.to(element, {
        scrollTrigger: {
          trigger: element,
          start: "top top",
          end: `${pinWrapWidth} bottom`,
          scroller: ".App", //locomotive-scroll
          scrub: 1,
          pin: true,
          // markers: true,
          // anticipatePin: 1,
        },
        height: `${scrollingElement.scrollWidth}px`,
        ease: "none",
      });

      t1.to(scrollingElement, {
        scrollTrigger: {
          trigger: scrollingElement,
          start: "top top",
          end: `${pinWrapWidth} bottom`,
          scroller: ".App", //locomotive-scroll
          scrub: 1,
          // markers: true,
        },
        x: -pinWrapWidth,

        ease: "none",
      });
      ScrollTrigger.refresh();
    }, 1000);
    ScrollTrigger.refresh();

    return () => {
      t1.kill();
      ScrollTrigger.kill();
    };
  }, []);

  return (
    <Section ref={ref} id="shop">
      <Title data-scroll data-scroll-speed="-1">
        Our Expertise
      </Title>
      <Left>
  <p>
    We specialize in crafting unforgettable experiences through beautifully 
    designed events.
    <br /><br />

    Whether it’s a luxurious wedding, a high-end corporate event, or an intimate 
    celebration, Chirayu delivers excellence in every detail — from decor and lighting 
    to sound and catering.
    <br /><br />

    Every event we create is a reflection of elegance, creativity, and flawless execution.
    <br /><br />

    At Chirayu, we don’t just organize events — we design experiences that stay with you forever.
  </p>
</Left>
      <Right data-scroll ref={Horizontalref}>
        <Product img={img3} title="Reception" />
        <Product img={img4} title="Sangeeth" />
        <Product img={img1} title="Haldi" />
        <Product img={img2} title="Coorporate events" />
        <Product img={img5} title="Engagments" />
        <Product img={img6} title="Celebrations" />
        <Product img={img7} title="Entry with Rose Theme" />
        <Product img={img8} title="Entry with Blue Theme" />
        <Product img={img9} title="Floral Entry" />
        <Product img={img10} title="Special Edition (Destination Wedding)" />
      </Right>
    </Section>
  );
};

